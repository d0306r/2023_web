package sec02.exam05;

public class StringExample {
    public static void main(String[] args) {
        // S는 대문자, 큰따옴표를 이용해야 함
        String name = "홍길동";
        String job = "프로그래머";
        String gender = "남자";

        System.out.println("name = " + name);
        System.out.println("job = " + job);
        System.out.println("gender = " + gender);
    }
}
