package sec02.exam08;

public class BooleanExample {
    public static void main(String[] args) {
        //java에서는 boolean 값을 소문자로만 쓴다.
        boolean stop = true;
        if (stop) {
            System.out.println("중지합니다.");
        } else {
            System.out.println("시작합니다.");
        }
    }
}
